public class TipoTuberia {
    private int circular;
    private int vertical;
    private int horizontal;
    private int source;
    private int drain;

    public int getCircular() {return circular;}
    public void setCircular(int circular) {this.circular = circular;}

    public int getVertical() {return vertical;}
    public void setVertical(int vertical) {this.vertical = vertical;}

    public int getHorizontal() {return horizontal;}
    public void setHorizontal(int horizontal) {this.horizontal = horizontal;}

    public int getSource() {return source;}
    public void setSource(int source) {this.source = source;}

    public int getDrain() {return drain;}
    public void setDrain(int drain) {this.drain = drain;}
}
